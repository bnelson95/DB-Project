<div id="info">
This database keeps track of the Parks and Rec organization. It is used to facilitate registration and information gathering by the administration of the organization, the coaches, and the parents of the players.
</div>

<br><br><br>

<h2 class="hometitle">How to Use the Database</h2>
<div id="info">
	<ol type="numbered">
		<li>The first step to using the Parks and Rec database is identifying what kind of user you are.  You can be a COACH, REGISTRAR, ADMINISTRATOR, GUARDIAN.</li>
		<li>Next you need to select the tab that applies to you (COACH, REGISTRAR, ADMINISTRATOR, GUARDIAN).</li>
		<li>You will then be prompted to select what kind of information you need to see.</li>
		<li>Select the type of information that you need.</li>
		<li>If the database needs more information for this request, you will be asked to provide it at this time.</li>
		<li>If all the requirements are met, the information will be shown to you.</li>
		<li>If you are updating information or adding new entries, you will be asked for the new information then shown the updated table.</li>
	</ol>
</div>
